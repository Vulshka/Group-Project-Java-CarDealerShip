    private void salesTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_salesTableMouseClicked
        // TODO add your handling code here:
        DBmanager db = new DBmanager();
        Connection con = db.getConnection();
        int noOfRow = salesTable.getSelectedRow();
        if (noOfRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table.", "No Row Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String click = salesTable.getModel().getValueAt(noOfRow, 0).toString();
        if (click == null || click.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Invalid.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (PreparedStatement ps = con.prepareStatement("SELECT * FROM tblsales WHERE SaleID = ?")) {
            ps.setString(1, click);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                salesID.setText(rs.getString("SaleID"));
                SF1.setText(rs.getString("YearSale"));
                SF2.setText(("CustomerName"));  
                SF3.setText( rs.getString("VehicleMake"));
                SF4.setText(rs.getString("VehicleModel"));
                SF5.setText(rs.getString("VehiclePrice"));
                } else {
                    JOptionPane.showMessageDialog(null, "Information not found", "Try Again", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "An error occurred while retrieving customer information. Please try again.", "Database Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace(); 
        }
    }//GEN-LAST:event_salesTableMouseClicked

