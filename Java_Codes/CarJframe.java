    private void customerTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customerTableMouseClicked
        // TODO add your handling code here:
        DBmanager db = new DBmanager();
        Connection con = db.getConnection();
        int noOfRow = customerTable.getSelectedRow();
        if (noOfRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table.", "No Row Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String click = customerTable.getModel().getValueAt(noOfRow, 0).toString();
        if (click == null || click.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Invalid.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try (PreparedStatement ps = con.prepareStatement("SELECT * FROM tblcustomer WHERE CustomerName = ?")) {
            ps.setString(1, click);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    CF7.setText(rs.getString("CustomerName"));
                    CF8.setText(rs.getString("CustomerEmail"));
                    CF9.setText(rs.getString("CustomerPhone"));
                    CF10.setText(rs.getString("CustomerAddress"));
                    customerID.setText(rs.getString("CustomerId"));
                } else {
                    JOptionPane.showMessageDialog(null, "Information not found", "Try Again", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "An error occurred while retrieving customer information. Please try again.", "Database Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();  // Optionally log the stack trace for debugging
        }
    }//GEN-LAST:event_customerTableMouseClicked

